package Encapsulation;

public class E1_Private
{
    private String name;
    private int age;

    // Setters.
    public void setName(String name)
    {
        this.name = name;
    }

    public void setAge(int age)
    {
        this.age = age;
    }

    // Getters.
    public String getName()
    {
        return name;
    }

    public int getAge()
    {
        return age;
    }
}
